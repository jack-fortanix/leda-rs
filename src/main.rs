pub unsafe extern "C" fn dump(mut n: *const libc::c_char,
                              mut b: *const uint8_t, mut l: size_t) {
    printf(b"%s = [%d] \x00" as *const u8 as *const libc::c_char, n, l);
    let mut i: size_t = 0i32 as size_t;
    while i != l {
        printf(b"%02X\x00" as *const u8 as *const libc::c_char,
               *b.offset(i as isize) as libc::c_int);
        i = i.wrapping_add(1)
    }
    printf(b"\n\x00" as *const u8 as *const libc::c_char);
    fflush(stdout);
}
